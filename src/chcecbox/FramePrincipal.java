    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        // TODO add your handling code here:
        if (jTextFieldNombre.getText().isEmpty() || jTextFieldApellidos.getText().isEmpty() || jTextFieldDni.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los campos estan vacios");
        } else {
            nombre = jTextFieldNombre.getText();
            apellidos = jTextFieldApellidos.getText();
            dni = jTextFieldDni.getText().toUpperCase();
            if (grupoCheckbox.getSelection() == null) {
                JOptionPane.showMessageDialog(this, "No has introducido el estado.");
            } else if (jCheckBoxParo.isSelected()) {
                estado = jCheckBoxParo.getText();
                seguir();
            } else if (jCheckBoxEst.isSelected()) {
                estado = jCheckBoxEst.getText();
                seguir();
            } else if (jCheckBoxTrab.isSelected()) {
                estado = jCheckBoxTrab.getText();
                seguir();
            }
        }

    }//GEN-LAST:event_jButtonAceptarActionPerformed

