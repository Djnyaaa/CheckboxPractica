    private void seguir() {
        sexo = (String) jComboSexo.getSelectedItem();
        if (sexo.contentEquals("Selecciona...")) {
            JOptionPane.showMessageDialog(this, "No has seleccionado el sexo de la persona.");
        } else {
            if (!nie.validarNIF(dni)) {
                JOptionPane.showMessageDialog(this, "El DNI que ha ingresado no es valido.");
                jTextFieldDni.setText("");
            } else {
                nie.establecerNIF(dni);
                Persona p = new Persona(nombre, apellidos, nie, sexo, estado);
                listaPersonas.add(p);
                System.out.println(p);
            }
        }
    }
    
    private void imprimirListaFichero() throws FileNotFoundException{
        PrintWriter fichero = new PrintWriter(new FileOutputStream("src/EstatSolicitants.txt"));
        for (int k=0; k< listaPersonas.size();k++) {
            fichero.println(listaPersonas.get(k));
        }
        fichero.close();
    }
